/**
 * Describes multi button css classes
 */
export interface MultiButtonCssClasses
{
    /**
     * Main button div
     */
    mainButtonDiv?: string;

    /**
     * Main button content div
     */
    mainButtonContentDiv?: string;

    /**
     * Main button separator div
     */
    mainButtonSeparatorDiv?: string;

    /**
     * Main button click area div
     */
    mainButtonClickAreaDiv?: string;

    /**
     * Main button option div
     */
    mainButtonMultiOptionDiv?: string;

    /**
     * Div for displaying sub buttons
     */
    subButtonsDiv?: string;
}